def build_pairs() -> pd.DataFrame:
    """
    역할: 모든 유형의 NLI 쌍을 하나의 DataFrame으로 조립
    입력: 없음 (전역 상수 사용)
    출력: premise, hypothesis, label 컬럼 DataFrame
    """
    rows = []

    for text in DIRECT_CRISIS:
        rows.append({"premise": text, "hypothesis": HYPOTHESIS, "label": ENTAILMENT})

    for text in INDIRECT_CRISIS:
        rows.append({"premise": text, "hypothesis": HYPOTHESIS, "label": ENTAILMENT})

    for text in METAPHOR_CRISIS:
        rows.append({"premise": text, "hypothesis": HYPOTHESIS, "label": NEUTRAL})

    for text in NON_CRISIS:
        rows.append({"premise": text, "hypothesis": HYPOTHESIS, "label": CONTRADICTION})

    for text in NEUTRAL_EXAMPLES:
        rows.append({"premise": text, "hypothesis": HYPOTHESIS, "label": NEUTRAL})

    return pd.DataFrame(rows)


def main():
    print("=" * 60)
    print("NLI 위기 감지 데이터 생성 시작")
    print("=" * 60)

    df = build_pairs()

    label_names = {ENTAILMENT: "entailment(위기)", NEUTRAL: "neutral", CONTRADICTION: "contradiction(비위기)"}
    print(f"\n[레이블 분포]")
    for lbl, name in label_names.items():
        count = (df["label"] == lbl).sum()
        print(f"  {name}: {count}쌍")
    print(f"  총계: {len(df)}쌍")

    out_path = os.path.join(OUT_DIR, "nli_pairs.csv")
    df.to_csv(out_path, index=False, encoding="utf-8-sig")

    print(f"\n[저장 완료] → {out_path}")
    print("=" * 60)


if __name__ == "__main__":
    main()
